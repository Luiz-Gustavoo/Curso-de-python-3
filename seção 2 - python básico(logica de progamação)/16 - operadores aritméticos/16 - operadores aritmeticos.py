# +, -. /, *, %, **, //

print(10 * 10)
print(10 + 10)

# divisão arredondada
print(10 // 2)

# divisão comum
print(10 / 2)

# multiplacação como multiplicador de string
print(10 * 'banana ')
print(10 * '<> ')

# concatenação de strings
print('200' + '200')

# convertendo tipo de dados
print(str(20))
print(type(20))

print(10 % 2)

print(10 ** 2)

# precedência dos operadores aritméticos
print((10 + 10) * 2)

